<?php

class OpcionRespuesta {

    private $opcId;
    private $preId;
    private $opcTipoRespuesta;
    private $opcArchivo;
    private $opcTexto;
    private $opcVerdadero;

    function getOpcId() {
        return $this->opcId;
    }

    function getPreId() {
        return $this->preId;
    }

    function getOpcTipoRespuesta() {
        return $this->opcTipoRespuesta;
    }

    function getOpcArchivo() {
        return $this->opcArchivo;
    }

    function getOpcTexto() {
        return $this->opcTexto;
    }

    function getOpcVerdadero() {
        return $this->opcVerdadero;
    }

    function setOpcId($opcId) {
        $this->opcId = $opcId;
    }

    function setPreId($preId) {
        $this->preId = $preId;
    }

    function setOpcTipoRespuesta($opcTipoRespuesta) {
        $this->opcTipoRespuesta = $opcTipoRespuesta;
    }

    function setOpcArchivo($opcArchivo) {
        $this->opcArchivo = $opcArchivo;
    }

    function setOpcTexto($opcTexto) {
        $this->opcTexto = $opcTexto;
    }

    function setOpcVerdadero($opcVerdadero) {
        $this->opcVerdadero = $opcVerdadero;
    }

}
